from math import ceil, floor

def kara(x,y):
    if x<10 and y <10:
        return x*y

    n = max(len(str(x)),len(str(y)))
    m = ceil(n/2)

    x_h = floor(x/10**m)
    x_l = x % (10*m)
    y_h = floor(y/10**m)
    y_l = y % (10*m)

    a = kara(x_h,y_h)
    d = kara(x_l,y_l)
    e = kara(x_h+x_l,y_h+y_l)-a-d

    return int(a*(10**(m*2))+e*(10**m)+d)

